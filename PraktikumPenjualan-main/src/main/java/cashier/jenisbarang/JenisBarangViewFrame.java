    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int rowSelected = tabelJenisBarang.getSelectedRow();
        if(rowSelected >=0){
        int pilihan =JOptionPane.showConfirmDialog(null, 
                "Yakin hapus?",
                "Konfirmasi", 
                JOptionPane.YES_NO_OPTION);    
        

        if (pilihan == 0){
            
            TableModel model = tabelJenisBarang.getModel();
            int id = (int) model.getValueAt(rowSelected, 0);
            Database db = new Database();
            Connection con = db.getConnection();
            
            JenisBarang jb = new JenisBarang(con);
            jb.setId(id);
            jb.delete();
        }
        
        }else{
            JOptionPane.showMessageDialog(null, "Pilih dulu datanya");
        }
        
        
        
        
    }//GEN-LAST:event_bHapusActionPerformed

