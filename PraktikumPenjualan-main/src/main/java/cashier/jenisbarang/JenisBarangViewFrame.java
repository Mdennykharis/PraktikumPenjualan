    private void mengisiTable(ArrayList<Object>list) {
        Object[] row = new Object[2];
        
        DefaultTableModel model =  (DefaultTableModel) tabelJenisBarang.getModel();
        model.setRowCount(0);
        
        for (int i = 0; i < list.size(); i++) {
            JenisBarang item = (JenisBarang) list.get(i);
            row[0] = item.getId();
            row[1] = item.getNamaJenisBarang();
            model.addRow(row);
        }
    }

    private void reset() {
        Database db = new Database();
        Connection con = db.getConnection();
        
        JenisBarang jb = new JenisBarang(con);
        ArrayList<Object> list = jb.read();
        
        mengisiTable(list);
    }

